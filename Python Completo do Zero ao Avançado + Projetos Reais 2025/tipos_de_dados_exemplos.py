# TIPOS DE DADOS EM PYTHON - EXEMPLOS SIMPLES E EXPLICATIVOS
# Use este arquivo para revisar os tipos básicos em Python!

# 1. int (números inteiros)
idade = 25  # Exemplo de inteiro
print('Idade:', idade, '| Tipo:', type(idade))

# 2. float (números decimais)
peso = 70.5  # Exemplo de float
print('Peso:', peso, '| Tipo:', type(peso))

# 3. str (texto, string)
nome = 'Maria'  # Exemplo de string
print('Nome:', nome, '| Tipo:', type(nome))

# 4. bool (booleano: True ou False)
estudando = True  # Exemplo de booleano
print('Está estudando?', estudando, '| Tipo:', type(estudando))

# 5. list (lista: coleção mutável de itens)
frutas = ['maçã', 'banana', 'uva']  # Exemplo de lista
print('Frutas:', frutas, '| Tipo:', type(frutas))

# 6. tuple (tupla: coleção imutável de itens)
coordenadas = (10, 20)  # Exemplo de tupla
print('Coordenadas:', coordenadas, '| Tipo:', type(coordenadas))

# 7. dict (dicionário: pares chave-valor)
pessoa = {'nome': 'João', 'idade': 30}  # Exemplo de dicionário
print('Pessoa:', pessoa, '| Tipo:', type(pessoa))

# 8. set (conjunto: coleção de itens únicos, sem ordem)
letras = {'a', 'b', 'c', 'a'}  # O set elimina duplicatas automaticamente
print('Letras:', letras, '| Tipo:', type(letras))

# DICA: Para saber o tipo de qualquer variável, use a função type()
# Exemplo:
print(type(3.14))  # float
print(type('abc'))  # str

# FIM DOS EXEMPLOS
